int		ft_isalpha(c)
{
	if ((c >= 65 && c <= 90) | (c >= 97 && c <= 122))
		return 1;
	return 0;
}

int main()
{
	char c = 'a';

	printf("%d",ft_isalpha(c));

	return 0;
}